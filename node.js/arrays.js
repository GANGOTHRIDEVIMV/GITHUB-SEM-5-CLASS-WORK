var students = ["Raj", "Vinay", "Sam"]
console.log(students.length)

students.shift("Karuna")
console.log(students)
//shift will remove elements from front of array

var students = ["Raj", "Vinay", "Sam"]
console.log(students.length)

students.unshift("Karuna")
console.log(students)
//unshift will add elements from front of array